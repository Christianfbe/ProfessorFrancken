  <h1>Committees</h1>

  <hr>

  <h2>Bincie (Dutch Field Trip Committee)</h2>

  <p>The Bincie, or the Dutch Field Trip Committee, aims to organize several field trips a year to companies or research centres in the Netherlands. Mini-field trips to departments are also organized to enable you to have a look at future internship places. Nowadays the Dutch Field Trip Committee also organizes some lunchtime lectures. </p>

  <hr>

  <h2>Borrelcie (Party Committee)</h2>

  <p>No society can exist without get-togethers, and that is certainly the case with T.F.V. ‘Professor Francken’! At Francken they are organized by the Borrelcie, or the Party Committee, who make sure that there is regularly something to celebrate at a pub or in the Francken Room, and that the celebration actually takes place. From barbecues to beer-drinking competitions, the Party Committee gets things really humming whilst ensuring that the place is not completely wrecked. To keep up with future activities watch your membership e-mails or check the site regularly. </p>
  
  <hr>

  <h2>Brouwcie (Brewing Committee)</h2>

  <p>(DUTCH PAGE) De Brouwcie vond haar oorsprong in een groep op bier georiënteerde studenten van de TFV. Voeg bij het idee om eens zelf een biertje te brouwen bij een vleugje ‘gewoon gaan’ en het duurt niet lang voordat er een pruttelende ketel op het vuur staat. De Commissie is nu al toe aan zijn tweede en verbeterde brouwinstallatie en blijft stevig door automatiseren.</p>

  <hr>

  <h2>Buixie (Foreign Field Trip Committee)</h2>

  <p>Every year, at the end of the third examination period, the Buixie, or the Foreign Field Trip Committee, organizes a trip to a European country. Last year we went to Scotland! Various activities take place during the trip, including visits to companies and universities to give the participants some idea of what goes on in the world of applied physics outside Groningen. At least one case study has to be carried out for the trip: this is usually arranged by one of the Association´s sponsors.</p>

  <hr>

  <h2>Compucie (Computer Committee)</h2>

  <p>Francken has had its own server since 2007. This hosts the website, the files of the board, committees and members, and music. The Compucie, or the Computer Committee, is responsible for keeping the server and the board’s and members’ workstations up and running. </p>
  
  <hr>

  <h2>Parties</h2>

  <p>Francken is super awesome, as the other study associations can testify! Which is why they are keen to organize parties with us.</p>

  <hr>

  <h2>Fotocie (Photo Committee)</h2>

  <p>A Franckenmember is supposed to be in good condition. Fortunately there is a committee that takes responsibility for this. The Fraccie takes care of the state of our ears, eyes, minds, livers and senses, so that our Franckenmembers remain their beauty. The training schedule consists among other things movie nights, the yearly jam session, members weekends, the poker competition and the Christmas dinner. Keep an eye on the posters and sign up at the form on the Franckendoor. </p>

  <hr>

  <h2>Francken Vrij Committee</h2>

  <p>One of the few regular events in the student lives of Francken members is the appearance of our popular science magazine Francken Vrij. It comes out three times a year and began its nineteenth volume this academic year. Each edition contains some regular physics columns, looking for instance at one of the applied physics departments at the University of Groningen. There is also space devoted to the Association’s activities, such as the field trips and drinks parties organized by the committees.</p>

