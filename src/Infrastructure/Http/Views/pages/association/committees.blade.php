  <h2>Kascie (Finance Committee) </h2>

  <p>
      The Finance Committee is a committee of the General Membership Assembly that is responsible for overseeing the association’s financial management. It presents a written report of its findings to the Assembly, which is read out there by one of the committee members. It is also tasked with advising the board’s treasurer on request or on its own initiative.
  </p>

  <a class="btn btn-primary" href="/about/committees/kascie">Visit the Kascie page</a>

  <br/>

  <h2>Oefensescie (Practice Session Committee) </h2>

  <p>
      The Practice Session Committee ensures that 10-15 practice sessions a year are held in difficult first-year subjects such as calculus, linear algebra and mechanics. Old exam papers are discussed under the supervision of experienced senior students and first-year students have an opportunity to ask the questions that so perplex them.
  </p>

