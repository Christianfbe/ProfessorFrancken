  <h2>Sympcie (Symposium Committee) </h2>

  <p>
      Every year the Symposium Committee organizes a symposium on a variety of applied physics topics, at which the participants find out what kinds of research various universities are doing and what is involved in researching new products or applications in industry. In past years the symposiums have been held on the ‘Nature of Physics’, ‘Saved by Physics’, ‘Sport and Technology’ and ‘Medical Physics’.
  </p>

