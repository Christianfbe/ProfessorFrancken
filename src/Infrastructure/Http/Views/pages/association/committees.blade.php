  <h2>Brouwcie (Brewing Committee)</h2>

  <p>(DUTCH PAGE) De Brouwcie vond haar oorsprong in een groep op bier georiënteerde studenten van de TFV. Voeg bij het idee om eens zelf een biertje te brouwen bij een vleugje ‘gewoon gaan’ en het duurt niet lang voordat er een pruttelende ketel op het vuur staat. De Commissie is nu al toe aan zijn tweede en verbeterde brouwinstallatie en blijft stevig door automatiseren.</p>

  <a class="btn btn-primary" href="/about/committees/brouwcie">Visit the Brouwcie page</a>

