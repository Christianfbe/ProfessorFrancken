  <h2>Borrelcie (Party Committee)</h2>

  <p>No society can exist without get-togethers, and that is certainly the case with T.F.V. ‘Professor Francken’! At Francken they are organized by the Borrelcie, or the Party Committee, who make sure that there is regularly something to celebrate at a pub or in the Francken Room, and that the celebration actually takes place. From barbecues to beer-drinking competitions, the Party Committee gets things really humming whilst ensuring that the place is not completely wrecked. To keep up with future activities watch your membership e-mails or check the site regularly. </p>

  <a class="btn btn-primary" href="/about/committees/borrelcie">Visit the Borrelcie page</a>

